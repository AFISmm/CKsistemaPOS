/**
 * Lista de paises para el selector de indicativo telefonico del formulario de
 * registro (/login, pestana "Registrarse"). DEMO: nombres solo en ingles (no
 * se tradujeron a los ~195 paises por alcance/tiempo — es una simplificacion
 * de esta demo, no una limitacion tecnica del selector en si).
 *
 * La bandera se GENERA en runtime a partir del codigo ISO 3166-1 alpha-2
 * (ver `banderaDesdeCodigoPais` abajo) componiendo los dos "regional
 * indicator symbols" Unicode correspondientes, en vez de guardar 195 emojis
 * copiados a mano (mas corto y sin errores de tipeo).
 */

export interface Pais {
  nombre: string;
  /** ISO 3166-1 alpha-2, ej. "US", "MX". */
  codigo: string;
  /** Indicativo telefonico E.164 con "+", ej. "+1", "+52". */
  indicativo: string;
}

/** Convierte un codigo ISO2 (ej. "US") a su emoji de bandera (🇺🇸). */
export function banderaDesdeCodigoPais(codigo: string): string {
  const BASE_REGIONAL_INDICATOR = 0x1f1e6; // "🇦"
  const letras = codigo.toUpperCase().split("");
  return letras
    .map((letra) => String.fromCodePoint(BASE_REGIONAL_INDICATOR + (letra.charCodeAt(0) - 65)))
    .join("");
}

